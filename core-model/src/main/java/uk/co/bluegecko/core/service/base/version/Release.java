package uk.co.bluegecko.core.service.base.version;


/**
 * Representation of various release stages.
 *
 */
public enum Release
{
	/**
	 * Not released.
	 */
	NONE,
	/**
	 * Snapshot release.
	 */
	SNAPSHOT,
	/**
	 * Release Candidate.
	 */
	RC,
	/**
	 * Alpha Release.
	 */
	ALPHA,
	/**
	 * Beta Release.
	 */
	BETA,
	/**
	 * Full Release.
	 */
	RELEASE
}
